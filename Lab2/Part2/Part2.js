var canvas = document.getElementById("myCanvas");
var context = canvas.getContext("2d");
context.beginPath();
var gradient1 = context.createRadialGradient(200, 200, 0, 180, 60, 200);
gradient1.addColorStop(0, "white");
gradient1.addColorStop(1, "blue");
context.arc(120, 120, 120, 0, 2 * Math.PI);
context.fillStyle = gradient1;
context.fill();
context.beginPath();
var gradient2 = context.createRadialGradient(180, 10, 0, 30, 100, 200);
gradient2.addColorStop(0, "white");
gradient2.addColorStop(1, "blue");
context.arc(120, 120, 90, 0, 2 * Math.PI);
context.fillStyle = gradient2;
context.fill();
context.fillStyle = "white"; 
context.font = "160px Arial";  
context.fillText("N", 65, 175);
